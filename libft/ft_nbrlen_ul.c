#include "libft.h"

int	ft_nbrlen_ul(unsigned long n, int base_size)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	while (n > 0)
	{
		n /= base_size;
		i++;
	}
	return (i);
}
